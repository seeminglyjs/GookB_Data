package java02_Operator_3day;

public class BinaryEx_03 {

	public static void main(String[] args) {
		
		//이항연산자 - 관계, 비교
		
		// <,  >,  <=,  >=,  ==,  !=
		
		int num1 = 10;
		int num2 = 20;
		
		System.out.println(num1 == num2); //false
		System.out.println(num1 != num2); //true
		System.out.println(num1 >= num2); //false
		System.out.println(num1 <= num2); //true
		System.out.println(num1 > num2); //false
		System.out.println(num1 < num2); // true
		
	}

}
