//package선언 - 소스코드가 포함된 패키지 
package java01_variables;

//클래스 정의 코드 
public class VariablesEx_03 {

	public static void main(String[] args) {
		
		int num;
		//쓰레기값이 들어가 있는 상태가 된다.
		num = 10;
		
		System.out.println(num);
		
		int num2 = 123;//선언과 동시에 초기화
		
		System.out.println(num2);
		
	}//메인 메소드 정의 끝

} // 클래스 정의 끝
