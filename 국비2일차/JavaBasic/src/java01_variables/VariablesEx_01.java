//package선언 - 소스코드가 포함된 패키지 
package java01_variables;

//클래스 정의 코드 
public class VariablesEx_01 {

	public static void main(String[] args) {
			
		
		System.out.println(123); // 정수형 int형
		System.out.println(3.14);	// double 실수형
		System.out.println('Q'); //문자형 char
		System.out.println("Hello everyone"); // 문자열 String
		System.out.println(true); //논리형 boolean
		
		
	}//메인 메소드 정의 끝

} // 클래스 정의 끝
