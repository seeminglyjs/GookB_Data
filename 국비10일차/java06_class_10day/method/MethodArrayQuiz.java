package java06_class_10day.method;

public class MethodArrayQuiz {

	public void mul10(int[] arr) {
		for(int i =0; i < arr.length; i++) {
			System.out.print((arr[i] * 10) + " ");
		}
		System.out.println();
	}

	public void print(int[] arr) {
		for(int i =0; i < arr.length; i++) {
			System.out.print(arr[i] + " ");
		}
		System.out.println();
	}

}
