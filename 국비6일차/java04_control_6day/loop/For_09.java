package java04_control_6day.loop;

public class For_09 {
	public static void main(String[] args) {

		//for 문과 2중 for문
		
		for(int i = 0; i < 3; i++) {
			for(int j = 0; j < 3; j++) {
				System.out.println(i + " / " + j);
			}
			System.out.println("--------------");
		}
		
	}

}
